# Create buildings
for i, (x, y, w, d, h) in enumerate(buildings):
    bpy.ops.mesh.primitive_cube_add(size=1, location=(x, y, h/2))
    obj = bpy.context.active_object
    obj.scale = (w, d, h)
    obj.name = f"Building_{i:03d}"
    
    # Apply scale
    bpy.ops.object.transform_apply(scale=True)
    
    # Add a grey material
    mat = bpy.data.materials.new(name=f"BuildingMat_{i:03d}")
    mat.use_nodes = True
    bsdf = mat.node_tree.nodes.get("Principled BSDF")
    if bsdf:
        grey = 0.3 + (i % 5) * 0.05  # slight color variation
        bsdf.inputs["Base Color"].default_value = (grey, grey, grey + 0.02, 1.0)
        bsdf.inputs["Roughness"].default_value = 0.8
    obj.data.materials.append(mat)

print(f"Created {len(buildings)} buildings")
