def _config_path():
    return os.path.join(OUTPUT_DIR, f"_{MODE}_camera_config.json")

def save_camera_config(ortho_scale_used):
    cfg = {
        "mode": MODE,
        "cam_distance": CAM_DISTANCE, "cam_height": CAM_HEIGHT,
        "char_center_z": CHAR_CENTER_Z,
        "frame_width": FRAME_WIDTH, "frame_height": FRAME_HEIGHT,
        "ortho_scale": ortho_scale_used,
    }
    with open(_config_path(), 'w') as f:
        json.dump(cfg, f, indent=2)
    print(f"  Config saved: {_config_path()}")

def load_camera_config():
    """Return saved ortho_scale if config exists, else None."""
    p = _config_path()
    if not os.path.exists(p):
        return None
    try:
        with open(p) as f:
            val = json.load(f).get("ortho_scale")
        print(f"  Loaded {MODE} config: ortho_scale={val}")
        return val
    except Exception:
        return None

