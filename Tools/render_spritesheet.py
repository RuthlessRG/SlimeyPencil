def render_direction(cam_obj, dir_name, angle_deg, output_dir):
    """Render all frames for one direction.

    In mixamo mode the camera re-centres on the armature every frame,
    so root-motion animations stay perfectly framed throughout.
    In meshy mode the camera is fixed (character is in-place).
    """
    dir_folder = os.path.join(output_dir, dir_name)
    os.makedirs(dir_folder, exist_ok=True)

    scene = bpy.context.scene
    rendered = skipped = 0

    for frame in range(FRAME_START, FRAME_END + 1, FRAME_STEP):
        filepath = os.path.join(dir_folder, f"frame_{frame:04d}.png")

        if SKIP_EXISTING and os.path.exists(filepath):
            skipped += 1
            continue

        scene.frame_set(frame)
        bpy.context.view_layer.update()   # force pose eval (needed for GLB/NLA rigs)
        target = get_char_center_this_frame()
        position_camera(cam_obj, angle_deg, target)

        scene.render.filepath = filepath
        bpy.ops.render.render(write_still=True)
        rendered += 1

        extra = f"  center=({target.x:.2f},{target.y:.2f})" if MODE == "mixamo" else ""
        print(f"  {dir_name} frame {frame}/{FRAME_END}{extra}")

    if skipped:
        print(f"  Skipped {skipped} already-rendered frames")
    return rendered

# ══════════════════════════════════════════════════════════════
#  STRIP / SHEET ASSEMBLY
#
#  PIL path (recommended — install Pillow in Blender's Python):
#    Assembles strips one frame at a time.  Only one frame ever
#    lives in RAM.  Safe for 500+ frames.
#
#  Blender fallback (no Pillow):
#    Loads all frame pixels as Python float lists simultaneously.
#    Memory usage: ~37 MB per frame at 512×640 → crashes ~80-100 frames.
#    CHUNK_SIZE limits each strip to keep it under that threshold.
# ══════════════════════════════════════════════════════════════

def _write_strip_pil(frame_paths, out_path):
    """Assemble a strip from file paths using PIL — one frame at a time."""
    from PIL import Image as PILImage
    first  = PILImage.open(frame_paths[0]).convert("RGBA")
    fw, fh = first.size
    strip  = PILImage.new("RGBA", (fw * len(frame_paths), fh))
    strip.paste(first, (0, 0))
    for i, fp in enumerate(frame_paths[1:], start=1):
        strip.paste(PILImage.open(fp).convert("RGBA"), (i * fw, 0))
    strip.save(out_path)
    print(f"  Saved: {out_path}  ({len(frame_paths)} frames, PIL)")

def _write_strip_blender(frame_paths, out_path):
    """Assemble a strip using Blender's image API — fallback when PIL is absent."""
    first_img = bpy.data.images.load(frame_paths[0])
    fw, fh    = first_img.size[0], first_img.size[1]
    n         = len(frame_paths)
    img       = bpy.data.images.new(os.path.basename(out_path),
                                     width=fw * n, height=fh, alpha=True)
    px = [0.0] * (fw * n * fh * 4)
    for fi, fp in enumerate([frame_paths[0]] + list(frame_paths[1:])):
        src    = first_img if fi == 0 else bpy.data.images.load(fp)
        src_px = list(src.pixels)
        for y in range(fh):
            for x in range(fw):
                si = (y * fw + x) * 4
                di = (y * fw * n + fi * fw + x) * 4
                px[di:di+4] = src_px[si:si+4]
        bpy.data.images.remove(src)
    img.pixels = px
    img.filepath_raw = out_path
    img.file_format  = 'PNG'
    img.save()
    bpy.data.images.remove(img)
    print(f"  Saved: {out_path}  ({n} frames, Blender fallback)")

def _write_strip(frame_paths, out_path):
    """Write a strip, using PIL if available, Blender pixel API otherwise."""
    try:
        _write_strip_pil(frame_paths, out_path)
    except ImportError:
        _write_strip_blender(frame_paths, out_path)

def combine_to_strip(dir_name, output_dir, num_frames):
    """Combine individual frame PNGs into strip(s).

    Returns list of (part_label, strip_path) — one entry normally,
    multiple when CHUNK_SIZE splits a long animation.

    With PIL installed: no frame-count limit (assembles one frame at a time).
    Without PIL: limited by CHUNK_SIZE to avoid out-of-memory crashes.
    """
    dir_folder   = os.path.join(output_dir, dir_name)
    all_paths    = [
        os.path.join(dir_folder, f"frame_{frame:04d}.png")
        for frame in range(FRAME_START, FRAME_END + 1, FRAME_STEP)
    ]

    # CHUNK_SIZE always respected — PIL just removes the memory crash risk.
    chunk = CHUNK_SIZE if CHUNK_SIZE > 0 else num_frames
    try:
        import PIL  # noqa — just confirming available for _write_strip
    except ImportError:
        if CHUNK_SIZE == 0 or chunk > 40:
            chunk = 40

    results = []

    if num_frames <= chunk:
        out = os.path.join(output_dir, f"{ANIM_NAME}_{dir_name}.png")
        _write_strip(all_paths, out)
        results.append((ANIM_NAME, out))
    else:
        n_parts = math.ceil(num_frames / chunk)
        print(f"  {num_frames} frames → {n_parts} parts of {chunk}  "
              f"(install Pillow to remove this limit)")
        for pi in range(n_parts):
            # Insert underscore when ANIM_NAME already ends with a digit
            # so "attack1" → "attack1_2", not "attack12"
            sep   = "_" if ANIM_NAME and ANIM_NAME[-1].isdigit() else ""
            label = f"{ANIM_NAME}{sep}{pi+1}"
            paths    = all_paths[pi*chunk : (pi+1)*chunk]
            part_dir = os.path.join(output_dir, label)
            os.makedirs(part_dir, exist_ok=True)
            out = os.path.join(part_dir, f"{label}_{dir_name}.png")
            _write_strip(paths, out)
            results.append((label, out))

    return results

