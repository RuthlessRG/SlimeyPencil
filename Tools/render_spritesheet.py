def main():
    global FRAME_START, FRAME_END
    _resolve_defaults()

    if FRAME_START is None or FRAME_END is None:
        detected_start, detected_end = detect_anim_frame_range()
        if FRAME_START is None: FRAME_START = detected_start
        if FRAME_END   is None: FRAME_END   = detected_end

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    num_frames    = len(range(FRAME_START, FRAME_END + 1, FRAME_STEP))
    total_renders = num_frames * len(DIRECTIONS)

    print("=" * 60)
    print(f"  SPRITE RENDERER  —  {MODE.upper()} MODE")
    print("=" * 60)
    if MODE == "mixamo":
        print("  Mixamo import reminder:")
        print("    File > Import > FBX  |  Scale=0.01  |  Pre/Post Rotation: ON")
        print()
    try:
        import PIL
        pil_status = f"Pillow {PIL.__version__} ✓  (no frame-count limit)"
        frame_limit = "unlimited"
    except ImportError:
        pil_status  = "Pillow NOT installed  ← install it to remove frame limits"
        frame_limit = f"{CHUNK_SIZE} per strip (Blender fallback)"

    print(f"  Anim:        {ANIM_NAME}")
    print(f"  Frames:      {FRAME_START}–{FRAME_END}  step={FRAME_STEP}  ({num_frames} frames)")
    print(f"  Resolution:  {FRAME_WIDTH}×{FRAME_HEIGHT}")
    print(f"  Renders:     {total_renders}  ({len(DIRECTIONS)} dirs × {num_frames} frames)")
    print(f"  Strip limit: {frame_limit}")
    print(f"  Pillow:      {pil_status}")
    print(f"  Output:      {OUTPUT_DIR}")
    print()

    # ── [1/6] Determine ortho_scale ───────────────────────────
    if MODE == "mixamo" and AUTO_FIT_CAMERA:
        print("[1/6] Auto-fitting camera (scanning bounding box)...")
        bbox = _scan_bbox_relative_to_center()
        ortho_scale = _calc_auto_ortho_scale(bbox) if bbox else ORTHO_SCALE
    else:
        saved = load_camera_config()
        ortho_scale = saved if saved is not None else ORTHO_SCALE
        src = "loaded from config" if saved is not None else "using ORTHO_SCALE default"
        print(f"[1/6] ortho_scale = {ortho_scale}  ({src})")

    print(f"  → ortho_scale = {ortho_scale:.3f}\n")

    # ── [2/6] Render setup ────────────────────────────────────
    print("[2/6] Render settings...")
    setup_render_settings()

    # ── [3/6] Camera ──────────────────────────────────────────
    print("[3/6] Camera...")
    cam = setup_camera(ortho_scale)

    # ── [4/6] Lighting + materials ────────────────────────────
    print("[4/6] Lighting + materials...")
    setup_lighting()
    make_materials_matte()

    # ── [5/6] Render each direction ───────────────────────────
    print(f"\n[5/6] Rendering...")
    dir_results = {}
    for dir_name, angle in DIRECTIONS:
        print(f"\n  --- {dir_name.upper()} ({angle}°) ---")
        render_direction(cam, dir_name, angle, OUTPUT_DIR)
        dir_results[dir_name] = combine_to_strip(dir_name, OUTPUT_DIR, num_frames)

    # ── [6/6] Cleanup, config, flip ───────────────────────────
    print("\n[6/6] Cleanup...")
    for dir_name, _ in DIRECTIONS:
        folder = os.path.join(OUTPUT_DIR, dir_name)
        if os.path.isdir(folder):
            shutil.rmtree(folder)
            print(f"  Deleted temp folder: {dir_name}/")

    save_camera_config(ortho_scale)

    # ── Summary ───────────────────────────────────────────────
    chunked  = CHUNK_SIZE > 0 and num_frames > CHUNK_SIZE
    n_parts  = math.ceil(num_frames / CHUNK_SIZE) if chunked else 1
    print("\n" + "=" * 60)
    print("  DONE!")
    print(f"  Mode:        {MODE}")
    print(f"  ortho_scale: {ortho_scale:.3f}")
    print(f"  Resolution:  {FRAME_WIDTH}×{FRAME_HEIGHT}")
    if chunked:
        print(f"  Output:      {n_parts} parts × 5 directions in {OUTPUT_DIR}")
        for i in range(1, n_parts + 1):
            label = f"{ANIM_NAME}{i}"
            print(f"    {label}/  ({label}_s, _n, _e, _ne, _se)")
    else:
        print(f"  Output:      {OUTPUT_DIR}")
        dirs_str = ", ".join(f"{ANIM_NAME}_{d}" for d, _ in DIRECTIONS)
        print(f"  Rendered:    {dirs_str}")
    print("=" * 60)

