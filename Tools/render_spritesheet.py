def _get_mesh_objects():
    return [o for o in bpy.context.scene.objects if o.type == 'MESH' and o.visible_get()]

def get_char_center_this_frame():
    """Return the world-space point the camera should orbit this frame.

    mixamo mode: reads the armature's actual world translation each frame,
                 so root-motion animations never leave the frame.
    meshy mode:  returns world origin + CHAR_CENTER_Z (character is static).
    """
    if MODE == "mixamo":
        # 1. Prefer armature — Mixamo rigs always have one
        for obj in bpy.context.scene.objects:
            if obj.type == 'ARMATURE' and obj.visible_get():
                loc = obj.matrix_world.translation
                return Vector((loc.x, loc.y, loc.z + CHAR_CENTER_Z))
        # 2. Fallback: average bounding box of all meshes
        meshes = _get_mesh_objects()
        if meshes:
            pts = [obj.matrix_world @ Vector(c)
                   for obj in meshes for c in obj.bound_box]
            xs = [p.x for p in pts]; ys = [p.y for p in pts]
            return Vector(((min(xs)+max(xs))*0.5,
                           (min(ys)+max(ys))*0.5,
                           CHAR_CENTER_Z))
    # meshy / fallback
    return Vector((0.0, 0.0, CHAR_CENTER_Z))

def position_camera(cam_obj, angle_deg, target=None):
    """Orbit the camera around target at the given horizontal angle."""
    if target is None:
        target = Vector((0.0, 0.0, CHAR_CENTER_Z))
    a = math.radians(angle_deg)
    cam_obj.location = Vector((
        target.x + math.sin(a) * CAM_DISTANCE,
        target.y - math.cos(a) * CAM_DISTANCE,
        target.z + CAM_HEIGHT,
    ))
    direction = target - cam_obj.location
    cam_obj.rotation_euler = direction.to_track_quat('-Z', 'Y').to_euler()

# ══════════════════════════════════════════════════════════════
#  AUTO-FIT ORTHO SCALE  (mixamo mode only)
# ══════════════════════════════════════════════════════════════

def _scan_bbox_relative_to_center():
    """Sample ~30 frames and measure the bounding box RELATIVE to the
    character's own center each frame.  This isolates limb-swing from
    root-motion translation so the result is accurate for any direction."""
    scene  = bpy.context.scene
    meshes = _get_mesh_objects()
    if not meshes:
        print("  WARNING: No visible meshes found — auto-fit skipped.")
        return None

    INF = float('inf')
    mn = [INF, INF, INF]; mx = [-INF, -INF, -INF]
    step = max(1, (FRAME_END - FRAME_START + 1) // 30)

    for frame in range(FRAME_START, FRAME_END + 1, step):
        scene.frame_set(frame)
        center = get_char_center_this_frame()
        for obj in meshes:
            for corner in obj.bound_box:
                wp  = obj.matrix_world @ Vector(corner)
                rel = [wp.x - center.x, wp.y - center.y, wp.z - center.z]
                for i in range(3):
                    if rel[i] < mn[i]: mn[i] = rel[i]
                    if rel[i] > mx[i]: mx[i] = rel[i]

    print(f"  BBox (relative to char center):")
    print(f"    X[{mn[0]:.2f}, {mx[0]:.2f}]  "
          f"Y[{mn[1]:.2f}, {mx[1]:.2f}]  "
          f"Z[{mn[2]:.2f}, {mx[2]:.2f}]")
    return tuple(mn + mx)

def _calc_auto_ortho_scale(bbox):
    """Convert bounding box to the ortho_scale that fits the character
    from any of the 5 camera angles with ORTHO_PADDING breathing room."""
    min_x, min_y, min_z, max_x, max_y, max_z = bbox
    aspect   = FRAME_WIDTH / FRAME_HEIGHT
    vertical = max_z - min_z
    xy_diag  = math.sqrt((max_x - min_x)**2 + (max_y - min_y)**2)
    needed   = max(vertical, xy_diag / aspect)
    result   = needed * ORTHO_PADDING
    print(f"  Auto-fit: Z_height={vertical:.2f}  XY_diag={xy_diag:.2f}  "
          f"needed={needed:.2f}  → ortho_scale={result:.3f}  "
          f"(×{ORTHO_PADDING} padding)")
    return result

