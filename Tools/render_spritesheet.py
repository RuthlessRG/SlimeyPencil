def _iter_action_fcurves(action):
    """Yield fcurves from an action — handles Blender 4.x and 5.0 APIs.

    Blender 4.x: action.fcurves (direct attribute, non-empty)
    Blender 5.0: action.layers[].strips[].channelbags[].fcurves (slotted system)
    If action.fcurves exists but is empty (5.0 compat shim), fall through to
    the layered system so keyframes are not missed.
    """
    if hasattr(action, 'fcurves'):
        curves = list(action.fcurves)
        if curves:
            yield from curves
            return
    for layer in getattr(action, 'layers', []):
        for strip in getattr(layer, 'strips', []):
            if hasattr(strip, 'fcurves'):
                yield from strip.fcurves
                continue
            for cb in getattr(strip, 'channelbags', []):
                yield from getattr(cb, 'fcurves', [])

def detect_anim_frame_range() -> tuple:
    """Read the actual keyframe extent by scanning f-curves and NLA strips.

    Method 1 — f-curve keyframe points in bpy.data.actions:
      Works for FBX imports where keyframes live directly on the action.

    Method 2 — NLA strip scene-space frame ranges:
      GLB/GLTF (Meshy) imports bake animations into NLA strips.  The action
      exists in bpy.data.actions but its fcurves have no keyframe points —
      all timing lives in the strip.  strip.frame_start/end are the scene-
      space frames that actually animate the character, so we use those.

    Falls back to scene.frame_start/end if nothing is found either way.
    """
    scene = bpy.context.scene
    start = float('inf')
    end   = float('-inf')

    # Method 1: f-curve keyframe points
    for action in bpy.data.actions:
        for fcurve in _iter_action_fcurves(action):
            for kp in fcurve.keyframe_points:
                if kp.co.x < start: start = kp.co.x
                if kp.co.x > end:   end   = kp.co.x

    # Method 2: NLA strips (GLB/Meshy baked animations have no raw keypoints)
    for obj in bpy.data.objects:
        ad = getattr(obj, 'animation_data', None)
        if ad is None: continue
        for track in getattr(ad, 'nla_tracks', []):
            for strip in track.strips:
                fs = getattr(strip, 'frame_start', None)
                fe = getattr(strip, 'frame_end',   None)
                if fs is not None and fs < start: start = fs
                if fe is not None and fe > end:   end   = fe

    if start == float('inf'):
        print("  Frame range: no keyframes or NLA strips found, using scene range")
        return int(scene.frame_start), int(scene.frame_end)
    s, e = int(round(start)), int(round(end))
    print(f"  Detected keyframe range: {s}–{e}  ({e - s + 1} frames)")
    return s, e

